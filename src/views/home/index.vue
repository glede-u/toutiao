<script>
import { getUserChannels } from '@/api/user'
import articleList from './components/article-list'
export default {
  name: 'homeIndex',

  data () {
    return {
      active: 0,
      userChannels: []
    }
  },
  components: {
    articleList
  },
  created () {
    this.loadUserChannels()
  },

  methods: {
    async loadUserChannels () {
      try {
        const { data } = await getUserChannels()
        this.userChannels = data.data.channels
      } catch (error) {
        this.$toast('获取频道列表失败')
      }
    }
  }
}
</script>

